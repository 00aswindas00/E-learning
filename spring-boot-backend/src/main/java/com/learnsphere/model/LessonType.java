package com.learnsphere.model;

/**
 * Enum representing the different types of lessons in the E-learning platform.
 */
public enum LessonType {
    VIDEO,       // Video-based lesson content
    TEXT,        // Text-based lesson content
    QUIZ,        // Quiz or assessment
    ASSIGNMENT,  // Assignment submission
    DISCUSSION,  // Discussion-based activity
    RESOURCE     // Downloadable resource
}
